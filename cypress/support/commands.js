Cypress.Commands.add('baseUrl', (url = null)=>{
    
      let defaultUrl = "https://paper.id/webappv1/#/invoicer/partner";

      if (url != null) {
            return cy.visit(url);
      }

      return cy.visit(defaultUrl);
})

